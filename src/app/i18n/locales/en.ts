type DeepWiden<T> =
	T extends string ? string :
	T extends readonly (infer U)[] ? readonly DeepWiden<U>[] :
	T extends object ? { [K in keyof T]: DeepWiden<T[K]> } :
	T;

export type EnLocale = DeepWiden<typeof en>;
